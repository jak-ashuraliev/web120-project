    <h2>SHOPPING CARTS</h2>
    <p>These days, there is a growing trend of online shopping thanks to the convenience of internet usage. We can’t deny that this trend creates significant changes in customers behaviors, or more clearly, the way people buy and spend on things. Customers can surf the web, find the products they want, purchase, and wait for those goods to be delivered right to their places while staying at home, sitting on their favorite couch, or lying on their comfortable bed. The easier online shopping is, the more demanding customers are towards the services. Therefore, e-stores have to force themselves to join in a competitive race, in which stores try to fulfill all of the customers’ need as perfectly and quickly as possible.</P>
    
    <p><strong>Online Shopping Cart</strong> plays the role of a physical shopping trolley in the supermarket but with more advanced functions. And of course, it is virtual. It is where shoppers can put all of their want-to-buy products in, review them to make adjustments in quantity, product attributes etc., and remove it before or during the checkout if they change their mind.</p>
    
    <p><strong>In order for a visitor to make an order through the web basket / shopping cart, you should have the following in mind:</strong></p>
    
    <ul>
        <li>Location - in the header of the site, ideally - in the upper right corner.</li>
        <li>The icon is intuitive, similar to a basket or bag.</li>
        <li>The icon should not look like a banner - such images are “filtered” by the user's view.</li>
        <li>In the upper corner of the online store next to the icon displays the number of goods that are in the basket, and their total value.</li>
        <li>If at least one product is added to the online shopping cart, its icon changes to another one, notifying that there is a product in it.</li>
    </ul>
    
    <p><strong>In the shopping cart</strong>, the buyer can work with products: view, change options and quantity, delete, add. The cart must give out the value of each item separately, as well as the total amount of the item. If the store works with discount coupons, then the visitor should have the opportunity to enter the coupon code and see the price of products with discounts.</p>
    
    <br>
    <img src="images/shopping-cart.jpg" alt="shopping cart illustration">
    <br>
    
    <p><strong>How to implement basket / shopping cart functionality correctly?</strong></p>
    
    <ol>
        <li>The shopping cart should display a photo of the goods, exactly the same as in the catalog. It is on the photo we focus, the product added or not.</li>
        <li>The name of the product must be clickable link. You can go to the item card, and once again view the characteristics or description, check whether you chose the product.</li>
        <li>Specify the payment system - how to pay for the goods or products. You need to notify the user now, and not when they wants to pay.</li>
        <li>Indicate whether the product is in stock or not. This information should be both in the item card and in the basket itself.</li>
        <li>Tell the user about promotions, wholesale prices, additional products and services - this way you will push the user to buy more.</li>
        <li>Allow the user to easily remove the product from the basket, while leaving the opportunity to add it to the wish list or return it if necessary.</li>
        <li>Add a button to return to purchases.</li>
        <li>The overall design of the page should repeat the design of the site, not differ from it. This reinforces the brand and does not confuse the user.</li>
        <li>The basket should not be overloaded with unnecessary details.</li>
        <li>Add the button <strong>“Quick order”</strong> or <strong>“Buy in one click”</strong>, due to which the buyer does not need to fill out a large order form. In this case, it is enough to leave the phone number.</li>
    </ol>
    
    <p><strong>Other important things to keep in mind:</strong></p>
    
    <ul>
        <li>The purchase button next to the product should indicate the action: <strong>“order”, “buy”</strong>, etc. If it contains a <stong>“I want”</strong> text, then this can be interpreted as a usual wish, but not a purchase.</li>
        <li>If the button for making a purchase does not contain text - it should be noticeable, large and have a clear icon.</li>
        <li>The <strong>“Buy”</strong> button should be placed on all pages of the site where the product is presented: both on the blocks with a brief description (the catalog itself) and when going to the product page.</li>
        <li>In the cart itself, a person should not be confused. For example, if he wants to increase the number of products, then the simple buttons <strong>“+” and “-”</strong> should help him in solving this issue.</li>
        <li>The person at any time should be able to return from the ordering mechanism to the online store itself.</li>
        <li>The return button should be noticeable and bright. Good functionality will be an indication of the cost of delivery right in the cart. After placing an order, <strong>“polite”</strong> online store always thanks its buyer.</li>
    </ul>
    
    <p><strong>Other solutions:</strong></p>
    
    <p>Hosted solutions are often recommended for those who are beginning their own venture because they require less technical expertise. That's not to say all new online retail store owners aren't adept in IT, but a hosted solution may free up time to focus on other aspects of operations. There are numerous providers of shopping cart software on the market today, and companies must first evaluate their individual needs before making a final decision on which solution is best for them.</p>
    
    <p>In conclusion, the fast-paced development of online business makes it easier for stores to reach their target customers. However, it also means that customers can have more choices, more places to pay their money online. This requires e-stores to run a customer-driven business, and try their best to gain competitive advantages so as to attract more customers. Understanding the working mechanism of the <strong>online shopping cart</strong> can be the key to help eCommerce store obtain these advantages, increase conversion rate, enhance their sales effectiveness, and avoid abandoned carts as much as possible.</p>
    
